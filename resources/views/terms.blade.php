                            (3) การเข้าถึงหรือการใช้ที่ไม่ได้รับอนุญาตใดๆ ซึ่งเซิร์ฟเวอร์รักษาความปลอดภัยของเรา และ/หรือข้อมูลส่วนบุคคลใดๆ ทั้งปวง และ/หรือข้อมูลทางการเงินที่เก็บอยู่ในเซิร์ฟเวอร์ดังกล่าว
                            (4) การหยุดชะงักหรือการหยุดของการส่งหรือรับบริการของเรา
                            (4) บั๊ก ไวรัส ม้าโทรจัน หรืออื่นๆที่อาจส่งไปหรือผ่านบริการของเราโดยบุคคลภายนอก
                            (5) ข้อผิดพลาดหรือการละเว้นในเนื้อหาใดหรือความสูญเสียหรือเสียหายประเภทใดก็ตามที่เกิดขึ้นโดยเป็นผลมาจากการใช้เนื้อหาใดที่โพสต์ อีเมล ส่ง หรือทำให้เข้าถึงผ่านบริการได้โดยประการอื่น ไม่ว่าที่เกี่ยวข้องกับการรับรอง สัญญา การละเมิด หรือทฤษฎีทางกฎหมายอื่นใด และไม่ว่าบริษัทจะได้รับแจ้งถึงความเป็นไปได้ในการเกิดค่าเสียหายดังกล่าวหรือไม่ การจำกัดความรับผิดดังกล่าวจะใช้บังคับมากเท่าที่กฎหมายอนุญาตในขอบเขตอำนาจที่เกี่ยวข้อง</p>

