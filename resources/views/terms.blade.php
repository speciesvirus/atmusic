                        <p>คุณตกลง รับผิดชดใช้จากสิทธิเรียกร้อง ค่าเสียหาย ภาระผูกพัน ความสูญเสีย ความรับผิด ต้นทุนหรือหนี้ และค่าใช้จ่าย อันเกิดจาก
                            (1) การที่คุณใช้และเข้าถึงบริการ
                            (2) การที่คุณละเมิดข้อกำหนดใดของเงื่อนไขและข้อกำหนดในการให้บริการฉบับนี้
                            (3) การที่คุณละเมิดสิทธิของบุคคลภายนอกใด รวมถึงแต่ไม่จำกัดเพียงลิขสิทธิ์ สิทธิในทรัพย์สิน หรือสิทธิความเป็นส่วนตัว
                            (4) การเรียกร้องใดๆ ว่าเนื้อหาของคุณก่อให้เกิดความเสียหายต่อบุคคลภายนอก ภาระผูกพันในการปกป้องและรับผิดชดใช้นี้จะมีผลบังคับต่อไปหลังจากเงื่อนไขและข้อกำหนดในการให้บริการฉบับนี้และการใช้บริการของคุณสิ้นสุดลง</p>
                        <h2>12. ความสามารถในการยอมรับเงื่อนไขและข้อกำหนดในการให้บริการ</h2>
                        <p>คุณยืนยันว่าคุณอายุเกิน 18 ปีหรือเป็นผู้เยาว์ที่เป็นอิสระ หรือได้รับความยินยอมของพ่อแม่หรือผู้ปกครอง และมีความสามารถโดยสมบูรณ์ในการเข้าทำข้อกำหนด เงื่อนไข ภาระผูกพัน การยืนยัน การรับรอง และการรับประกันที่ระบุในข้อกำหนดการบริการฉบับนี้ และจะปฏิบัติตามเงื่อนไขและข้อกำหนดในการให้บริการฉบับนี้ ในกรณีใดก็ตาม คุณรับรองว่าคุณอายุมากกว่า 13 ปี เนื่องจากบริการไม่มีวัตถุประสงค์ให้เด็กอายุต่ำกว่า 13 ปีใช้ หากคุณอายุต่ำกว่า 13 ปี โปรดหลีกเลี่ยงการใช้บริการ มีเว็บไซต์ดีๆ อื่นๆ มากมายสำหรับคุณ โปรดคุยกับพ่อแม่ของคุณเพื่อหาเว็บไซต์ที่เหมาะสม</p>


